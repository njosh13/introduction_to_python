# variables
ineteger = 4
float_number = 4.5

# sum
sum = ineteger + float_number
print('sum: {}'.format(sum))

# difference
dif = ineteger - float_number
print('dif: {}'.format(dif))

# product
prod = ineteger * float_number
print('prod: {}'.format(prod))

# Exponentiation
exp = 4 ** 4  # 4^4
print('exp:', exp)

# Modulo
mod = 3 % 2
print('mod:', mod)

# Div always returns a float number
div = 3/2
print('div: {}' .format(div))


