# Boolean = True or False

true_boolean = True
false_boolean = False
print('true:{0} false:{1}' .format(true_boolean, false_boolean))


# Comparators

# Equal To
eq = 1 == 2
print('equal to: ', eq)

# greater than
gr = 1 > 2
print('greater than: ', gr)

# greater or equal to
gr_eq = 1 >= 2
print('greater or equal to : ', gr_eq)

# lesser than
ls = 1 < 2
print('lesser than: ', ls)

# Not equal to
not_eq = 1 != 2
print('Not equal to: ', not_eq)

# And , or , Not
# And
and_value = ls and not_eq
print('and_value: ', and_value)

# or
or_value = ls or eq
print('or_value: ', or_value)

# not
not_value = not ls
print('not_value: ', not_value)
